
public class ConditionalOperators {

	public static void main(String[] args) {
		int value1 = 1;
        int value2 = 3;
        
      boolean bharath=(value1==1)&&(value2==2); //And operator
      System.out.println(bharath);
      
      boolean flag1=(value1==1)||(value2==2); //OR Operators
      System.out.println(flag1);
      
      boolean x=true;
      boolean y=false;
      System.out.println(x&&y);
      System.out.println(x||y);
	}

}
